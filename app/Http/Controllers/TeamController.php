    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request): \Illuminate\Http\JsonResponse | null
    {
        $request->validate([
            'name' => 'required|unique:teams,name',
        ]);
        $teamCount = Team::count();
        $maxTeams = Team::MAX_TEAMS;

        if ($teamCount >= $maxTeams) {
            return response()->json(['message' => 'Team creation limit has been reached.'], 400);
        }
        $request->user()->teams()->create($request->only('name'));
    }

